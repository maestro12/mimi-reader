package com.emogoth.android.phone.mimi.view;

import android.content.Context;
import android.graphics.Bitmap;
import android.graphics.Canvas;
import android.graphics.Paint;
import android.graphics.PorterDuff;
import android.graphics.PorterDuffXfermode;
import android.graphics.Rect;
import android.graphics.drawable.BitmapDrawable;
import android.graphics.drawable.Drawable;
import androidx.appcompat.widget.AppCompatImageView;
import android.util.AttributeSet;


public class CircularImageView extends AppCompatImageView {


    public CircularImageView(Context context) {
        super(context);
    }

    public CircularImageView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public CircularImageView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
    }

    @Override
    public void setImageDrawable(Drawable drawable) {

        final Drawable img;
        if (drawable instanceof BitmapDrawable) {
            img = new BitmapDrawable(getContext().getResources(), getCircleImage(((BitmapDrawable) drawable).getBitmap()));
        } else {
            img = drawable;
        }

        super.setImageDrawable(img);

    }

    @Override
    public void setImageBitmap(Bitmap bm) {
        super.setImageBitmap(getCircleImage(bm));
    }

    public Bitmap getCircleImage(final Bitmap bitmap) {
        Bitmap output = Bitmap.createBitmap(bitmap.getWidth(),
                bitmap.getHeight(), Bitmap.Config.ARGB_8888);
        Canvas canvas = new Canvas(output);
        final Paint paint = new Paint();
        final Rect rect = new Rect(0, 0, bitmap.getWidth(),
                bitmap.getHeight());

        final int radius;
        if (bitmap.getHeight() > bitmap.getWidth()) {
            radius = bitmap.getWidth() / 2;
        } else {
            radius = bitmap.getHeight() / 2;
        }

        paint.setAntiAlias(true);
        canvas.drawARGB(0, 0, 0, 0);
        // paint.setColor(color);
        canvas.drawCircle(bitmap.getWidth() / 2,
                bitmap.getHeight() / 2, radius, paint);
        paint.setXfermode(new PorterDuffXfermode(PorterDuff.Mode.SRC_IN));
        canvas.drawBitmap(bitmap, rect, rect, paint);
        return output;
    }

}
